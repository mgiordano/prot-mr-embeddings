VECTOR_SIZE = 100

def get_vector_for_bioword_partition(bioword_partition, sequence, model):
    """Optimized embedding vector estimation for sequence based on its bioword representation"""

    # Enforce strict input: must be a non-empty string
    if not isinstance(bioword_partition, str):
        raise TypeError(f"bioword_partition must be str, got {type(bioword_partition).__name__}")

    tokens = list(tokenize(bioword_partition))
    if not tokens:
        # Use sequence as single token when tokens list is empty
        tokens.append(sequence)


    return model.wv.get_mean_vector(tokens, pre_normalize=False, post_normalize=True)

#@task(log_prints=True)
def compute_sequence_vectors(corpus_df, model, bioword_rule_column="word_partition", chunk_size=10000):
    """Optimized sequence vector computation with chunked processing and progress logging"""
    logging.info("START TASK - compute_sequence_vectors (chunked)")
    
    bio_vectors = []
    total_rows = len(corpus_df)
    
    # Process in chunks to reduce memory pressure and provide progress updates
    for i in range(0, total_rows, chunk_size):
        chunk_end = min(i + chunk_size, total_rows)
        chunk = corpus_df[bioword_rule_column].iloc[i:chunk_end]
        
        # Process chunk with optimized function
        # Get corresponding sequence chunk
        sequence_chunk = corpus_df["sequence"].iloc[i:chunk_end].astype(str)
        chunk_vectors = pd.concat([chunk.astype(str), sequence_chunk], axis=1).apply(
            lambda row: get_vector_for_bioword_partition(row.iloc[0], row.iloc[1], model), axis=1
        )
        bio_vectors.extend(chunk_vectors.tolist())
        
        # Log progress every 50k sequences
        if chunk_end % 50000 == 0 or chunk_end == total_rows:
            logging.info(f"Processed {chunk_end}/{total_rows} sequences ({(chunk_end/total_rows)*100:.1f}%)")
    
    corpus_df["bio_vector"] = bio_vectors
    logging.info("END TASK - compute_sequence_vectors (chunked)")
    return corpus_df

#@task(log_prints=True)
def load_corpus_eval_to_df(corpus_path: str):
    logging.info("START TASK - load_corpus_eval_to_df")
    df = pd.read_csv(corpus_path, encoding='utf-8', keep_default_na=False)
    logging.info("END TASK - load_corpus_eval_to_df")
    return df

#@task(log_prints=True)
def get_corpus_eval_file_path_from_run(input_data_root_path: str, family_dataset_name: str, timestamp: str, filter_name: str, partition_rule_name: str, is_control: bool = False):
    logging.info("START TASK - get_corpus_eval_file_path_from_run")
    if is_control:
        # Load control corpus file generated by corpus_prep_control.py
        corpus_path = corpus_prep_utils.get_control_corpus_file_path_from_run(input_data_root_path, family_dataset_name, timestamp, filter_name, partition_rule_name)
        if not os.path.exists(corpus_path):
            raise FileNotFoundError(f"Control corpus file not found at {corpus_path}. Please run corpus_prep_control.py first.")
    else:
        # Load regular family dataset corpus
        corpus_file_iterator = corpus_prep_utils.get_corpus_file_iterator_from_run(input_data_root_path, family_dataset_name, timestamp, filter_name, partition_rule_name, False)
        corpus_path = corpus_prep_utils.create_or_load_joined_corpus_file(corpus_file_iterator)
    logging.info("END TASK - get_corpus_eval_file_path_from_run")
    return corpus_path

#@task(log_prints=True)
def load_model(model_path: str, use_mmap: bool = False):
    logging.info("START TASK - load_model")
    if use_mmap:
        logging.info("Loading model with memory mapping (mmap='r') - slower but uses less RAM")
        model = FastText.load(model_path, mmap='r')
    else:
        logging.info("Loading model into RAM - faster but uses more memory")
        model = FastText.load(model_path)
    logging.info("END TASK - load_model")
    return model

#@task(log_prints=True)
def create_and_save_metadata(corpus_df, filename_prefix, parent_folder_path, is_control=False):
    """Extract metadata creation and saving logic into a reusable function.
    
    Dynamically selects all columns except internal/computed ones (word_partition, bio_vector)
    so it works with both original corpus format (family_name, family_type) and
    metadata-override format (superfamily, cath_class, etc.).
    """
    logging.info("START TASK -  save metadata.tsv")
    # save metadata tsv — use all columns except internal/computed ones
    exclude_columns = {"word_partition", "bio_vector"}
    metadata_columns = [col for col in corpus_df.columns if col not in exclude_columns]
    metadata_df = corpus_df[metadata_columns].copy()

    # For control datasets, set sequence_family_type to "control" and add partition_type column
    if is_control:
        if "sequence_family_type" in metadata_df.columns:
            metadata_df["sequence_family_type"] = "control"
        # Add partition_type column based on word_partition content
        if "word_partition" in corpus_df.columns:
            metadata_df["partition_type"] = np.where(corpus_df["word_partition"] == "", "sequence", "mr")
        else:
            logging.warning("word_partition column not found in corpus_df, cannot determine partition_type")

    metadata_filename = filename_prefix + "-metadata.tsv"
    metadata_out_path = os.path.join(parent_folder_path, metadata_filename)
    metadata_df.to_csv(metadata_out_path, sep='\t', index=False)
    logging.info("END TASK -  save metadata.tsv")

